jumin = "920922-1234567" 
print("성별 : " + jumin[7])  # 남:1, 여:2
print("출생년도 : " + jumin[0:2])
print("월 : " + jumin[2:4])
print("일 : " + jumin[4:6])
print("생년월일 : " + jumin[:6])
print("주민번호 뒷자리 : " + jumin[7:])
print("뒤 7자리 (뒤에서부터) : " + jumin[-7:])

